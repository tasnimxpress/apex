"             np.where(surv.status.eq('discontinued'), surv.days_to_discontinuation, np.nan))\n"
"dur_event = pd.Series(days_event, index=surv.index)/30.44\n"
"dur_censor = surv.tenure_to_date_months.astype(float)\n"
"surv['event'] = surv.churn_strict.astype(int)\n"
"surv['duration'] = np.where(surv.event==1, dur_event, dur_censor)\n"
"# 3 closed w/o closing date + 1 disc w/o date -> event date missing: censor them instead (flagged)\n"
"missing_evt = surv.event.eq(1) & surv.duration.isna()\n"
"surv.loc[missing_evt, 'duration'] = surv.loc[missing_evt,'tenure_to_date_months'].astype(float)\n"
"surv.loc[missing_evt, 'event'] = 0\n"
"surv['duration'] = surv.duration.astype(float).clip(lower=0.1)\n"
