"surv_tbl.columns = ['surv_12m','surv_24m','surv_36m']\n"
"C.save_table(surv_tbl, SEC, 'cohort_survival_km')\n"
"metrics['cohort_survival_km'] = surv_tbl.reset_index(names='onboarding_year').to_dict('records')\n"
"(surv_tbl*100).round(1)"
